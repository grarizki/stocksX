// Generates realistic 5-year monthly dummy history for each economic indicator
// Each dataset has 60 data points: Jan 2021 → Dec 2025

export type HistoryPoint = { date: string; value: number };

function monthLabels(count = 60): string[] {
	const result: string[] = [];
	const start = new Date(2021, 0, 1);
	for (let i = 0; i < count; i++) {
		const d = new Date(start);
		d.setMonth(d.getMonth() + i);
		result.push(d.toISOString().slice(0, 7)); // "YYYY-MM"
	}
	return result;
}

function series(values: number[]): HistoryPoint[] {
	const labels = monthLabels(values.length);
	return values.map((v, i) => ({ date: labels[i], value: v }));
}

